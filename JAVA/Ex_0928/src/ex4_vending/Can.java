package ex4_vending;

public class Can {
	
	private String name;
	private int price;
	
	//음료수 이름과 가격을 저장할 변수 
	public String getName() {
		return name;
	}
	public void setName(String name) {
		this.name = name;
	}
	public int getPrice() {
		return price;
	}
	public void setPrice(int price) {
		this.price = price;
	}
	
	
}
